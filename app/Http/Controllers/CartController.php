     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //crear un arreglo de producto para que almacenen la informacion de la session
        $producto = [
            [
                "id" => $request->prod_id,
                "name" => $request->prod_name,
                "cantidad" => $request->cantidad
            ]
        ];

        //crear una variable de session(persistencia en el sistema, el ciclo termina cuando se cierra el navegador)
        session(['cart' => $producto]);
     
       
       
    }

